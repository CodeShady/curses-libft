#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*str;
	char	*p;

	str = (char *)malloc(sizeof(char) * (size + 1));
	if (!str)
		return (NULL);
	p = str;
	while (size)
	{
		*(p++) = '\0';
		size--;
	}
	*p = '\0';
	return (str);
}
