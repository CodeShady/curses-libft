#include "libft.h"

static void	ft_fillspace(void *s, size_t n)
{
	while (n--)
		*(unsigned char *)s++ = 0;
}

void	*ft_calloc(size_t count, size_t n)
{
	void	*space;

	if (count && n && count > (UINT_MAX / n))
		return (NULL);
	space = malloc(count * n);
	if (!space)
		return (NULL);
	ft_fillspace(space, count * n);
	return (space);
}
